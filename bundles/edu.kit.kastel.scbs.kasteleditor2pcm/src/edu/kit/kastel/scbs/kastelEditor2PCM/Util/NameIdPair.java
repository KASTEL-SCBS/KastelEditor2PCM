package edu.kit.kastel.scbs.kastelEditor2PCM.Util;

public class NameIdPair {
	private final String name;
	private final String id;
	
	public NameIdPair(String name, String id) {
		this.name = name;
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getId() {
		return id;
	}
}
